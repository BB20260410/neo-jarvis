function clean(value, max = 2000) {
  return redactSensitiveText(String(value ?? '').trim()).slice(0, max);
}

function safeJson(value) {
  if (!value || typeof value !== 'object') return {};
  try {
    return JSON.parse(redactSensitiveText(JSON.stringify(value)));
  } catch {
    return {};
  }
}

export function normalizeNoeFreedomTool(input = {}) {
  const id = clean(input.id || input.operation, 180);
  if (!id) return null;
  return {
    schemaVersion: NOE_FREEDOM_MANIFEST_SCHEMA_VERSION,
    id,
    name: clean(input.name || input.title || id, 180),
    description: clean(input.description || '', 1200),
    version: clean(input.version || '1.0.0', 40),
    category: clean(input.category || 'freedom', 80),
    operation: clean(input.operation || id, 180),
    capability: clean(input.capability || input.operation || id, 120),
    riskLevel: clean(input.riskLevel || input.risk_level || 'critical', 40).toLowerCase(),
    dryRunSupported: input.dryRunSupported !== false,
    permissionRequired: input.permissionRequired !== false,
    tags: [...new Set([...(Array.isArray(input.tags) ? input.tags : []), ...(Array.isArray(input.capabilityTags) ? input.capabilityTags : [])].map((tag) => clean(tag, 80)).filter(Boolean))],
    rollback: safeJson(input.rollback),
    trust: {
      source: clean(input.trust?.source || input.source || 'builtin', 80),
      allowlistRequired: input.trust?.allowlistRequired !== false,
      evidenceRequired: input.trust?.evidenceRequired !== false,
      secretOutputDenied: input.trust?.secretOutputDenied !== false,
    },
  };
}

export function listNoeFreedomTools({ includeHidden = true } = {}) {
  const tools = BUILTIN_FREEDOM_TOOLS.map(normalizeNoeFreedomTool).filter(Boolean);
  return includeHidden ? tools : tools.filter((tool) => tool.riskLevel !== 'critical');
}

export function normalizeNoeFreedomQuickStart(input = {}) {
  const id = clean(input.id, 160);
  const actionId = clean(input.actionId || input.action || input.toolId, 180);
  if (!id || !actionId) return null;
  return {
    id,
    title: clean(input.title || id, 160),
    description: clean(input.description || '', 800),
    actionId,
    mode: NOE_FREEDOM_AUTH_MODES.has(clean(input.mode, 80)) ? clean(input.mode, 80) : 'developer_unrestricted',
    args: safeJson(input.args),
    tags: [...new Set((Array.isArray(input.tags) ? input.tags : []).map((tag) => clean(tag, 80)).filter(Boolean))],
  };
}

export function listNoeFreedomQuickStarts() {
  return BUILTIN_FREEDOM_QUICK_STARTS.map(normalizeNoeFreedomQuickStart).filter((item) => item && findNoeFreedomTool(item.actionId));
}

export function findNoeFreedomTool(id = '') {
  const target = clean(id, 180).toLowerCase();
  return listNoeFreedomTools().find((tool) => tool.id.toLowerCase() === target || tool.operation.toLowerCase() === target) || null;
}

