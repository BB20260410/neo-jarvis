export function validateNoeFreedomAuthorization({
  tool = null,
  authorization = {},
  realExecute = false,
} = {}) {
  const errors = [];
  const mode = clean(authorization.mode || (realExecute ? '' : 'dry_run'), 80);
  if (!NOE_FREEDOM_AUTH_MODES.has(mode)) errors.push('invalid_freedom_authorization_mode');
  const developerMode = mode === 'developer_unrestricted';
  if (realExecute && !['owner_supervised_unrestricted', 'developer_unrestricted'].includes(mode)) {
    errors.push('owner_supervised_unrestricted_required_for_real_execute');
  }
  if (realExecute && authorization.ownerPresent !== true) errors.push('owner_present_required_for_real_execute');
  if (realExecute && authorization.systemOverride === true) errors.push('system_level_override_denied');
  if (tool?.trust?.allowlistRequired && realExecute && !developerMode && authorization.allowlistAccepted !== true) errors.push('allowlist_acceptance_required');
  if (tool?.rollback?.requiredForRealExecute && realExecute && !developerMode && !clean(authorization.rollbackPlan || authorization.rollbackRef, 1000)) {
    errors.push('rollback_plan_required');
  }
  return {
    ok: errors.length === 0,
    errors,
    mode,
    ownerPresent: authorization.ownerPresent === true,
    realExecute: realExecute === true,
  };
}

function redactUrlLikeText(value = '') {
  const text = clean(value, 2000);
  if (!text) return '';
  try {
    const url = new URL(text);
    for (const key of [...url.searchParams.keys()]) {
      if (/token|key|secret|password|code|auth|session|credential|jwt/i.test(key)) {
        url.searchParams.set(key, '[redacted]');
      }
    }
    if (/token|key|secret|password|auth|session|credential|jwt/i.test(url.hash)) {
      url.hash = '#[redacted]';
    }
    return clean(url.toString(), 2000);
  } catch {
    return text.replace(/([?&#][^=]*?(?:token|key|secret|password|code|auth|session|credential|jwt)[^=]*=)[^&#\s]+/gi, '$1[redacted]');
  }
}

export function redactNoeFreedomPayload(value) {
  if (typeof value === 'string') return redactUrlLikeText(value);
  if (typeof value === 'number' || typeof value === 'boolean' || value == null) return value;
  if (Array.isArray(value)) return value.slice(0, 50).map(redactNoeFreedomPayload);
  if (!value || typeof value !== 'object') return clean(value, 400);
  const looksLikeBrowserDomAction = Boolean(value.selector || value.type || value.kind || value.action);
  const out = {};
  for (const [key, item] of Object.entries(value).slice(0, 80)) {
    const k = clean(key, 120);
    const sensitiveKey = /secret|token|key|password|authorization|credential|cookie/i.test(k);
    const browserDomValueKey = looksLikeBrowserDomAction && /^(value|text|content)$/i.test(k);
    out[k] = sensitiveKey && !(typeof item === 'number' || typeof item === 'boolean' || item == null)
      ? '[redacted]'
      : browserDomValueKey && !(typeof item === 'number' || typeof item === 'boolean' || item == null)
      ? '[redacted]'
      : redactNoeFreedomPayload(item);
  }
  return out;
}
